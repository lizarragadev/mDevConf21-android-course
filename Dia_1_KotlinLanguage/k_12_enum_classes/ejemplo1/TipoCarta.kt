package k_12_enum_classes.ejemplo1

/**
 *
 * Un ENUM nada más es una representación donde podemos almacenar
 * datos estáticos para posteriormente utilizarlos en
 * distintos contextos.
 * En este caso tenemos un ENUM con datos estáticos de los nombres de las
 * cartas de una baraja.
 * Un ENUM comienza con la instrucción enum class.
 *
 * */

enum class TipoCarta {
    DIAMANTE, TREBOL, CORAZON, ESPADA
}