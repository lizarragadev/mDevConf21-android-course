package k_14_interfaces.ejemplo1

/**
 *
 * Interfaces.
 * Una interface sirve para definir el comportamiento de una
 * serie de objetos y métodos.
 * Una de las carácterísticas principales de una interface es que solamente
 * se declaran las funciones y no asi se realiza su implementación.
 * En el siguiente ejemplo solo declaramos la función imprimir de la cual
 * realizaremos su implementación en otras clases.
 *
 * */

interface Punto {
    fun imprimir()
}